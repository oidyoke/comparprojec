package io.renren.modules.sys.entity;

public class OtherGasEntity {
    private String company;
    private String total;
    private String methane;
    private String propane;
    private String otherGas;

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getMethane() {
        return methane;
    }

    public void setMethane(String methane) {
        this.methane = methane;
    }

    public String getPropane() {
        return propane;
    }

    public void setPropane(String propane) {
        this.propane = propane;
    }

    public String getOtherGas() {
        return otherGas;
    }

    public void setOtherGas(String otherGas) {
        this.otherGas = otherGas;
    }
}

